namespace AutoCrudAdmin.Enumerations;

/// <summary>
/// The type of the form control.
/// </summary>
public enum FormControlType
{
    /// <summary>
    /// Automatic form control type.
    /// </summary>
    Auto = 0,

    /// <summary>
    /// Multi-choice check box form control type.
    /// </summary>
    MultiChoiceCheckbox = 1,

    /// <summary>
    /// Text area form control type.
    /// </summary>
    TextArea = 2,

    /// <summary>
    /// Autocomplete form control type.
    /// </summary>
    Autocomplete = 3,
}